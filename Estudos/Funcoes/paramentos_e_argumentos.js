/* Parâmetros e argumentos

    * Parâmetros são os elementos que irão receber dados, ou, de onde os dados serão coletados para que a função realize o comando desejado

    * Argumentos são os valores/dados que determinamos ou coletamos para os parâmetros
 */

// Parâmetros (parameters)
const soma = function(number1, number2) {
    console.log(number1 + number2)
}

soma(2, 3) // argumentos -> 2 será o number1 e o 3 será o number2